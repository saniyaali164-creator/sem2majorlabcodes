public class task3 {
    public static void main(String[] args){
        double celsius = 25.0;
        // Conversion formula: F = (C × 9/5) + 32
        double fahrenheit = (celsius * 9 / 5) + 32;
        System.out.println("Celsius: " + celsius + "°C");
        System.out.println("Fahrenheit: " + fahrenheit + "°F");}
}
